    public void populateTable() {
       DefaultTableModel dtm = (DefaultTableModel) deliverymanTable.getModel();
        dtm.setRowCount(0);
        for (DeliveryMan dm : system.getDeliveryManDirectory().getDeliverymanList()) {
            Object row[] = new Object[2];
            row[0] = dm;
            row[1] = dm.getName();
            dtm.addRow(row);
        }
  
    }
