        double temprature = Double.parseDouble(temptxt.getText());
        double bloodPressure = Double.parseDouble(BPtxt.getText());
        int pulse = Integer.parseInt((pulsetxt.getText()));
        String date = datetxt.getText();
        
        VitalSigns vs = vitalhistory.addNewVital();
        vs.setTemperature(temprature);
        vs.setBloodPressure(bloodPressure);
        vs.setPulse(pulse);
        vs.setDate(date);
        
        JOptionPane.showMessageDialog(this, "New Vital Signs added");
        temptxt.setText("");
        BPtxt.setText("");
        pulsetxt.setText("");
        datetxt.setText("");
